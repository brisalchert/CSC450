package Module7;

public class PaintColor {
    private String color;

    // Constructor initializes color to "red"
    public PaintColor() {
        this.color = "red";
    }

    // Accessor for color
    public String getColor() {
        return color;
    }

    // Method to set color to "green"
    public void setColorGreen() {
        this.color = "green";
    }

    // Method to set color to "blue"
    public void setColorBlue() {
        this.color = "blue";
    }

    // Method to set color to "red"
    public void setColorRed() {
        this.color = "red";
    }
}
